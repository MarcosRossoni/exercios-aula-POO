package meupacote;

public class Moto {

    public String marca;

    public String modelo;

    public String cor;

    public int marcha;

    public Moto() {
    }

    public Moto(String marca, String modelo, String cor, int marcha) {
        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.marcha = marcha;
    }
}
